package com.camara.organizada.controllers;

public class DeputyDto {
	
	private String initJob;
	private int approvedLaws;
	private String party;
	public String getInitJob() {
		return initJob;
	}
	public void setInitJob(String initJob) {
		this.initJob = initJob;
	}
	public int getApprovedLaws() {
		return approvedLaws;
	}
	public void setApprovedLaws(int approvedLaws) {
		this.approvedLaws = approvedLaws;
	}
	public String getParty() {
		return party;
	}
	public void setParty(String party) {
		this.party = party;
	}
	

}
